/**
 * TypeScript Registry Generator
 *
 * Generates TypeScript registry tables and lookup functions from TTD schema.
 */

/**
 * Convert name to SCREAMING_SNAKE_CASE
 */
function toConstantCase(name) {
  return name
    .replace(/([A-Z])/g, '_$1')
    .toUpperCase()
    .replace(/^_/, '');
}

/**
 * Generate TypeScript registry from TTD schema
 */
export function generateTsRegistry(schema) {
  const lines = [];

  lines.push('/**');
  lines.push(' * TTD Generated Registry');
  lines.push(' *');
  lines.push(' * Auto-generated by continuum/wesley/ttd');
  lines.push(' * DO NOT EDIT');
  lines.push(' */');
  lines.push('');

  // Schema hash constant
  if (schema.schemaHash) {
    lines.push('/** SHA-256 hash of the source schema */');
    lines.push(`export const SCHEMA_HASH = '${schema.schemaHash}';`);
    lines.push('');
  }

  // Type ID constants
  lines.push('// Type ID constants');
  for (const entry of schema.registry || []) {
    const constName = `${toConstantCase(entry.typeName)}_TYPE_ID`;
    lines.push(`export const ${constName} = ${entry.id};`);
  }
  lines.push('');

  // Op ID constants
  lines.push('// Operation ID constants');
  for (const op of schema.ops || []) {
    const constName = `${toConstantCase(op.name)}_OP_ID`;
    lines.push(`export const ${constName} = ${op.op_id};`);
  }
  lines.push('');

  // Type registry map
  lines.push('/** Type name to ID mapping */');
  lines.push('export const TYPE_REGISTRY = new Map<string, number>([');
  for (const entry of schema.registry || []) {
    lines.push(`  ['${entry.typeName}', ${entry.id}],`);
  }
  lines.push(']);');
  lines.push('');

  // Op registry map
  lines.push('/** Operation name to ID mapping */');
  lines.push('export const OP_REGISTRY = new Map<string, number>([');
  for (const op of schema.ops || []) {
    lines.push(`  ['${op.name}', ${op.op_id}],`);
  }
  lines.push(']);');
  lines.push('');

  // Reverse maps
  lines.push('/** Type ID to name mapping */');
  lines.push('export const TYPE_REGISTRY_REV = new Map<number, string>([');
  for (const entry of schema.registry || []) {
    lines.push(`  [${entry.id}, '${entry.typeName}'],`);
  }
  lines.push(']);');
  lines.push('');

  lines.push('/** Operation ID to name mapping */');
  lines.push('export const OP_REGISTRY_REV = new Map<number, string>([');
  for (const op of schema.ops || []) {
    lines.push(`  [${op.op_id}, '${op.name}'],`);
  }
  lines.push(']);');
  lines.push('');

  // Lookup functions
  lines.push('/**');
  lines.push(' * Get type name by ID');
  lines.push(' */');
  lines.push('export function getTypeById(id: number): string | undefined {');
  lines.push('  return TYPE_REGISTRY_REV.get(id);');
  lines.push('}');
  lines.push('');

  lines.push('/**');
  lines.push(' * Get operation by ID');
  lines.push(' */');
  lines.push('export function getOpById(id: number): string | undefined {');
  lines.push('  return OP_REGISTRY_REV.get(id);');
  lines.push('}');
  lines.push('');

  lines.push('/**');
  lines.push(' * Get type ID by name');
  lines.push(' */');
  lines.push('export function getTypeIdByName(name: string): number | undefined {');
  lines.push('  return TYPE_REGISTRY.get(name);');
  lines.push('}');
  lines.push('');

  lines.push('/**');
  lines.push(' * Get operation ID by name');
  lines.push(' */');
  lines.push('export function getOpIdByName(name: string): number | undefined {');
  lines.push('  return OP_REGISTRY.get(name);');
  lines.push('}');

  return lines.join('\n');
}
