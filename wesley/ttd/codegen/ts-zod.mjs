/**
 * TypeScript Zod Validators Generator
 *
 * Generates Zod schemas for runtime validation from TTD schema.
 */

/**
 * Map GraphQL type to Zod validator
 */
function mapTypeToZod(gqlType) {
  const typeMap = {
    ID: 'z.string()',
    String: 'z.string()',
    Int: 'z.number().int()',
    Float: 'z.number()',
    Boolean: 'z.boolean()'
  };

  return typeMap[gqlType] || `z.lazy(() => ${gqlType}Schema)`;
}

function escapeSingleQuotedJsString(value) {
  return String(value)
    .replaceAll('\\', '\\\\')
    .replaceAll('\'', '\\\'')
    .replaceAll('\r', '\\r')
    .replaceAll('\n', '\\n')
    .replaceAll('\u2028', '\\u2028')
    .replaceAll('\u2029', '\\u2029');
}

/**
 * Generate constraint validators
 */
function generateConstraints(constraint) {
  if (!constraint) return '';

  const validators = [];

  // Use .gte()/.lte() for numeric bounds to avoid collision with string .min()/.max()
  if (constraint.min !== undefined) {
    validators.push(`.gte(${constraint.min})`);
  }
  if (constraint.max !== undefined) {
    validators.push(`.lte(${constraint.max})`);
  }
  if (constraint.minLength !== undefined) {
    validators.push(`.min(${constraint.minLength})`);
  }
  if (constraint.maxLength !== undefined) {
    validators.push(`.max(${constraint.maxLength})`);
  }
  if (constraint.pattern) {
    // Escape characters that would terminate or corrupt the regex literal.
    const escapedPattern = constraint.pattern
      .replace(/\\/g, '\\\\')
      .replace(/\//g, '\\/')
      .replace(/\r/g, '\\r')
      .replace(/\n/g, '\\n')
      .replace(/\u2028/g, '\\u2028')
      .replace(/\u2029/g, '\\u2029');
    validators.push(`.regex(/${escapedPattern}/)`);
  }
  if (constraint.oneOf && Array.isArray(constraint.oneOf) && constraint.oneOf.length > 0) {
    const serializedValues = constraint.oneOf.map(v =>
      typeof v === 'string' ? `'${escapeSingleQuotedJsString(v)}'` : v
    ).join(', ');
    const messageValues = constraint.oneOf.map(v =>
      typeof v === 'string' ? `"${v}"` : v
    ).join(', ');
    validators.push(`.refine(v => [${serializedValues}].includes(v), { message: "must be one of: ${messageValues}" })`);
  }

  return validators.join('');
}

/**
 * Generate Zod schemas from TTD schema
 */
export function generateTsZod(schema) {
  const lines = [];

  lines.push('/**');
  lines.push(' * TTD Generated Zod Validators');
  lines.push(' *');
  lines.push(' * Auto-generated by continuum/wesley/ttd');
  lines.push(' * DO NOT EDIT');
  lines.push(' */');
  lines.push('');
  lines.push("import { z } from 'zod';");
  lines.push('');

  // Generate enum schemas
  for (const enumDef of schema.enums || []) {
    const values = enumDef.values.map(v => `'${v}'`).join(', ');
    lines.push('/**');
    lines.push(` * Zod schema for ${enumDef.name}`);
    lines.push(' */');
    lines.push(`export const ${enumDef.name}Schema = z.enum([${values}]);`);
    lines.push(`export type ${enumDef.name} = z.infer<typeof ${enumDef.name}Schema>;`);
    lines.push('');
  }

  // Generate object schemas
  for (const typeDef of schema.types || []) {
    lines.push('/**');
    lines.push(` * Zod schema for ${typeDef.name}`);
    lines.push(' */');
    lines.push(`export const ${typeDef.name}Schema = z.object({`);

    for (const field of typeDef.fields || []) {
      let zodType = mapTypeToZod(field.type);

      // Add constraints
      const constraints = generateConstraints(field.constraint);
      if (constraints) {
        zodType = `${zodType}${constraints}`;
      }

      // Handle list types
      if (field.list) {
        zodType = `z.array(${zodType})`;
      }

      // Handle optional fields - use .nullish() for both undefined and null
      if (!field.required) {
        zodType = `${zodType}.nullish()`;
      }

      lines.push(`  ${field.name}: ${zodType},`);
    }

    lines.push('});');
    lines.push(`export type ${typeDef.name} = z.infer<typeof ${typeDef.name}Schema>;`);
    lines.push('');
  }

  // Generate union schemas after object schemas. Member references stay lazy so
  // object schemas may also refer back to the union without initialization
  // order becoming part of the contract.
  for (const unionDef of schema.unions || []) {
    const variants = unionDef.variants.map((variant) => `z.lazy(() => ${variant}Schema)`);
    const unionExpression = variants.length === 1
      ? variants[0]
      : `z.union([${variants.join(', ')}])`;

    lines.push('/**');
    lines.push(` * Zod schema for ${unionDef.name}`);
    lines.push(' */');
    lines.push(`export const ${unionDef.name}Schema = ${unionExpression};`);
    lines.push(`export type ${unionDef.name} = z.infer<typeof ${unionDef.name}Schema>;`);
    lines.push('');
  }

  // Generate per-op var/result schemas
  for (const op of schema.ops || []) {
    const pascal = `${op.name.charAt(0).toUpperCase()}${op.name.slice(1)}`;

    // Vars (args) schema
    if (op.args && op.args.length > 0) {
      const schemaName = `${pascal}ArgsSchema`;
      lines.push('/**');
      lines.push(` * Zod schema for ${op.name} arguments`);
      lines.push(' */');
      lines.push(`export const ${schemaName} = z.object({`);

      for (const arg of op.args) {
        let zodType = mapTypeToZod(arg.type);

        if (arg.list) {
          zodType = `z.array(${zodType})`;
        }

        if (!arg.required) {
          zodType = `${zodType}.optional()`;
        }

        lines.push(`  ${arg.name}: ${zodType},`);
      }

      lines.push('});');
      lines.push(`export type ${pascal}Args = z.infer<typeof ${schemaName}>;`);
      lines.push('');
    }

    // Result schema
    const rt = op.result_type ?? op.resultType;
    if (rt) {
      const resultSchemaName = `${pascal}ResultSchema`;
      let resultZod = mapTypeToZod(rt);
      if (op.result_list) resultZod = `z.array(${resultZod})`;
      if (op.result_required === false) resultZod += '.optional()';
      lines.push('/**');
      lines.push(` * Zod schema for ${op.name} result`);
      lines.push(' */');
      lines.push(`export const ${resultSchemaName} = ${resultZod};`);
      lines.push(`export type ${pascal}Result = z.infer<typeof ${resultSchemaName}>;`);
      lines.push('');
    }
  }

  return lines.join('\n');
}
